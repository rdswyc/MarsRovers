namespace MarsRovers
{
    /// <summary>
    /// Basic enumeration with the four possible headings: North, East, South and West.
    /// </summary>
    public enum Heading
    {
        N,
        E,
        S,
        W
    }

    /// <summary>
    /// Basic enumeration with the allowed instruction moves for the rover: Left, forward (M) or Right.
    /// </summary>
    public enum Instruction
    {
        L,
        M,
        R
    }
}
